function opt(isCorrect, ru, en) {
    return { isCorrect, ru, en };
}

function Q(num, difficulty, metadata, ru, en, options) {
    const slug = { EASY: 'easy', MEDIUM: 'medium', HARD: 'hard' }[difficulty];
    const id = `q-vg-${slug}-${String(num).padStart(3, '0')}`;

    return {
        id,
        difficulty,
        metadata,
        translations: { ru: { text: ru }, en: { text: en } },
        options: options.map((option, index) => ({
            order: index + 1,
            isCorrect: option.isCorrect,
            translations: {
                ru: { text: option.ru },
                en: { text: option.en },
            },
        })),
    };
}

